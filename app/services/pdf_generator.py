class NumberedJDCanvas(canvas.Canvas):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()  # type: ignore

    def save(self):
        num_pages = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self.draw_page_decorations(num_pages)
            super().showPage()
        super().save()

    def draw_page_decorations(self, page_count):
        self.saveState()
        self.setFont("DejaVuSans-Bold", 8)
        self.setFillColor(colors.HexColor("#4f46e5")) # Indigo accent
        
        # Running header
        self.drawString(36, 755, "ResumeIQ AI — Job Description Match Report")
        self.setFont("DejaVuSans", 8)
        self.setFillColor(colors.HexColor("#64748b"))
        self.drawRightString(576, 755, "Confidential Candidate-Job Alignment Evaluation")
        
        self.setStrokeColor(colors.HexColor("#e2e8f0"))
        self.setLineWidth(0.75)
        self.line(36, 747, 576, 747)
        
        # Running footer
        self.line(36, 42, 576, 42)
        self.drawString(36, 30, "Report generated by ResumeIQ 2.0 Engine")
        page_text = f"Page {self._pageNumber} of {page_count}"  # type: ignore
        self.drawRightString(576, 30, page_text)
        self.restoreState()


